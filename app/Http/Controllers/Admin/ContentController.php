    /**
     * Update the specified resource in storage.
     */
    public function update(Request $request, Content $content)
    {

        $request->validate([
            'target_keyword' => 'required|string|max:255',
            'hierarchy_level' => 'required|string|in:pillar,cluster,sub_cluster',
            'silo_blueprint_id' => 'required|exists:silo_blueprints,id',
            'body_raw' => 'nullable|string',
            'rendered_html_path' => 'nullable|string',
            'meta_title' => 'nullable|string|max:70',
            'meta_description' => 'nullable|string|max:160',
            'status' => 'required|string',
            'canonical' => 'nullable|url',
            'robots' => 'nullable|string',
            'og_title' => 'nullable|string',
            'og_description' => 'nullable|string',
            'og_image' => 'nullable|url',
            'twitter_card' => 'nullable|string|in:summary_large_image,summary',
            'schema_type' => 'nullable|string',
            'schema_custom_json' => 'nullable|string',
            'featured_image_upload' => 'nullable|image|mimes:jpeg,png,jpg,webp|max:2048',
            'featured_image_url' => 'nullable|string',
            'published_at' => 'nullable|date',
        ]);

        $updateData = $request->except(['canonical', 'robots', 'og_title', 'og_description', 'og_image', 'twitter_card', 'schema_type', 'schema_custom_json', 'featured_image_upload']);

        if ($request->hasFile('featured_image_upload')) {
            $result = app(\App\Services\ImageService::class)->processUpload(
                $request->file('featured_image_upload'), 'content-images', 1200
            );
            if ($result) {
                $updateData['featured_image_url'] = $result['url'];
            }
        }

        // Auto-generate OG image if publishing without featured_image
        if (($updateData['status'] ?? null) === 'published' && empty($updateData['featured_image_url']) && !$content->featured_image_url) {
            $title = $updateData['meta_title'] ?? $content->meta_title ?? $updateData['target_keyword'] ?? $content->target_keyword;
            $slug = $updateData['target_keyword'] ?? $content->target_keyword ?? 'content';
            $ogUrl = app(\App\Services\ImageService::class)->generateOgImage($title, $slug);
            if ($ogUrl) {
                $updateData['featured_image_url'] = $ogUrl;
            }
        }

        // Auto-set published_at when publishing
        if (($updateData['status'] ?? null) === 'published' && !$content->published_at) {
            $updateData['published_at'] = now();
        }

        $content->update($updateData);

        // Build schema
        $schema = null;
        if ($request->schema_custom_json) {
            $parsed = json_decode($request->schema_custom_json, true);
            if (is_array($parsed)) {
                $schema = $parsed;
            }
        }
        if (!$schema && $request->schema_type && $request->schema_type !== 'None') {
            $schema = ['@type' => $request->schema_type];
        }

        // Update polymorphic SEO meta
        $content->updateSeoMeta([
            'title' => $request->meta_title,
            'description' => $request->meta_description,
            'canonical' => $request->canonical,
            'robots' => $request->robots ?? 'index, follow',
            'og_title' => $request->og_title,
            'og_description' => $request->og_description,
            'og_image' => $request->og_image,
            'twitter_card' => $request->twitter_card ?? 'summary_large_image',
            'schema' => $schema,
        ]);

        return redirect()->route('admin.content.show', $content->id)
            ->with('success', 'Content successfully updated manually.');
    }

    /**
     * Trigger AI Generation for a manual Blueprint.
     */
    public function generateAi(Content $content)
    {
        // Set status to ai_processing
        $content->update(['status' => 'ai_processing']);

        // Check if there's already a pending/processing job
        $existingJob = AiGenerationJob::withoutGlobalScopes()
            ->where('content_id', $content->id)
            ->whereIn('status', ['pending', 'processing'])
            ->first();

        if (!$existingJob) {
            $job = AiGenerationJob::create([
                'tenant_id'  => $content->tenant_id ?? (\App\Models\Tenant::first()?->id ?? 1),
                'content_id' => $content->id,
                'job_type'   => 'initial_generation',
                'status'     => 'pending',
                'retry_count'=> 0
            ]);

            ProcessAiGenerationJob::dispatch($content->id, $job->id);
            
            // Trigger background worker for shared hosting / Windows safely
            $php = PHP_BINARY;
            $artisan = base_path('artisan');
            if (strtoupper(substr(PHP_OS, 0, 3)) === 'WIN') {
                if (function_exists('popen') && function_exists('pclose')) {
                    pclose(popen("start /B {$php} {$artisan} queue:work --stop-when-empty > NUL", "r"));
                }
            } else {
                if (function_exists('exec')) {
                    exec("{$php} {$artisan} queue:work --stop-when-empty > /dev/null 2>&1 &");
                }
            }
        }

        return redirect()->route('admin.content.create')->with('success', 'AI Generation started for ' . $content->title);
    }

    /**
     * Register content IDs for AI generation and return the list.
     * The frontend will call generateSingle() for each one sequentially via AJAX.
     */
    public function bulkGenerateAi(Request $request)
    {
        $request->validate([
            'content_ids'   => 'required|array',
            'content_ids.*' => 'exists:contents,id',
            'target_status' => 'nullable|string|in:draft,published',
        ]);

        $targetStatus = $request->input('target_status', 'draft');
        $tenantId     = \App\Models\Tenant::first()?->id ?? 1;
        $queue        = [];

        foreach ($request->content_ids as $id) {
            $content = Content::withoutGlobalScopes()->find($id);
            if (!$content || !in_array($content->status, ['blueprint', 'draft', 'needs_reoptimize', 'failed_cqi', 'ai_processing'])) {
                continue;
            }

            // Skip if already actively being processed
            $alreadyActive = AiGenerationJob::withoutGlobalScopes()
                ->where('content_id', $content->id)
                ->whereIn('status', ['pending', 'processing', 'phase_1', 'phase_2', 'phase_3', 'phase_4'])
                ->exists();
            if ($alreadyActive) continue;

            // Reuse existing job if it failed, so we can resume
            $job = AiGenerationJob::withoutGlobalScopes()
                ->where('content_id', $content->id)
                ->whereIn('status', ['failed', 'failed_cqi', 'completed'])
                ->first();

            if ($job) {
                $job->update([
                    'status'      => 'pending',
                    'retry_count' => $job->retry_count + 1,
                ]);
            } else {
                $job = AiGenerationJob::create([
                    'tenant_id'   => $content->tenant_id ?? $tenantId,
                    'content_id'  => $content->id,
                    'job_type'    => 'initial_generation',
                    'status'      => 'pending',
                    'retry_count' => 0,
                ]);
            }

            $content->update(['status' => 'ai_processing']);

            $queue[] = [
                'job_id'        => $job->id,
                'content_id'    => $content->id,
                'target_status' => $targetStatus,
                'keyword'       => $content->target_keyword,
                'title'         => $content->title,
            ];
        }

        return response()->json([
            'success' => true,
            'queue'   => $queue,
            'count'   => count($queue),
        ]);
    }

    /**
     * Generate a single content article synchronously.
     * Called by the frontend one content at a time via AJAX.
     * Runs the full Phase 1→4 pipeline in one HTTP request.
     */
    public function generateSingle(Request $request)
    {
        error_reporting(E_ALL & ~E_DEPRECATED & ~E_USER_DEPRECATED);
        @ini_set('display_errors', '0');
        @set_time_limit(300);
        @ini_set('memory_limit', '512M');

        // Gunakan StreamedResponse untuk mencegah Cloudflare 524 Timeout.
        // Cloudflare akan memutus koneksi jika tidak ada data yang dikirim selama 100 detik.
        // Dengan StreamedResponse, kita mengirim HTTP Header dan satu byte spasi terlebih dahulu.
        return response()->stream(function () use ($request) {
            // Enable keep-alive pings for streamed responses
            \App\Services\AIService::$progressPingsEnabled = true;

            // Kirim spasi untuk memulai response body dan mencegah timeout
            echo " ";
            if (ob_get_level() > 0) ob_flush();
            flush();

            // Eksekusi proses berat
            $result = $this->_doGenerateSingle($request);

            // Kirim JSON aslinya (spasi di awal akan diabaikan oleh JSON.parse di frontend)
            echo json_encode($result);
            if (ob_get_level() > 0) ob_flush();
            flush();
        }, 200, [
            'Content-Type'      => 'application/json',
            'X-Accel-Buffering' => 'no', // Disable Nginx buffering
            'Cache-Control'     => 'no-cache',
            'Connection'        => 'keep-alive',
        ]);
    }

    private function _doGenerateSingle(Request $request)
    {
        ignore_user_abort(true);

        try {
            $request->validate([
                'content_id'    => 'required|exists:contents,id',
                'job_id'        => 'required|exists:ai_generation_jobs,id',
                'target_status' => 'nullable|string|in:draft,published',
            ]);

            if ($request->hasSession()) {
                $request->session()->save();
            }

            $content      = Content::withoutGlobalScopes()->find($request->content_id);
            $job          = AiGenerationJob::withoutGlobalScopes()->find($request->job_id);
            $targetStatus = $request->input('target_status', 'draft');
            $logs         = [];

            if (!$content || !$job) {
                return ['success' => false, 'error' => 'Content or Job not found.', 'logs' => []];
            }

            $recovery = new \App\Services\AiRecoveryManager($content->tenant ?? null);
            if ($recovery->isCircuitOpen()) {
                return [
                    'success' => true,
                    'status'  => 'wait',
                    'keyword' => $content->target_keyword,
                    'logs'    => [['level' => 'warn', 'message' => 'Circuit Breaker aktif (Rate Limit API). Menunggu 90 detik...']],
                ];
            }

            if ($job->status === 'completed') {
                return [
                    'success' => true,
                    'keyword' => $content->target_keyword,
                    'title'   => $content->title,
                    'cqi'     => $content->cqi_score,
                    'status'  => 'completed',
                    'logs'    => [['level' => 'success', 'message' => '✅ Artikel selesai diproses oleh background queue.']],
                ];
            }

            if (in_array($job->status, ['failed', 'failed_cqi'])) {
                return [
                    'success' => false,
                    'keyword' => $content->target_keyword,
                    'error'   => $job->error_log['error'] ?? $job->error_log['reason'] ?? 'Job gagal diproses.',
                    'logs'    => [['level' => 'error', 'message' => '❌ Artikel gagal diproses.']],
                ];
            }

            $lockKey = "ai_job_lock_{$job->id}";
            $lock = \Illuminate\Support\Facades\Cache::lock($lockKey, 120);
            if (!$lock->get()) {
                return [
                    'success' => true,
                    'status'  => 'wait',
                    'keyword' => $content->target_keyword,
                    'logs'    => [['level' => 'running', 'message' => 'Proses masih berjalan di background queue...']],
                ];
            }

            try {
                $keyword = $content->target_keyword;

                // ── Step 1: Ensure job starts from phase_1 ──
                if (in_array($job->status, ['pending', 'failed_cqi'])) {
                    $job->update([
                        'status'      => 'phase_1',
                        'started_at'  => now(),
                        'retry_count' => ($job->status === 'failed_cqi') ? ($job->retry_count + 1) : 0,
                    ]);
                }

                // Reset stale data when starting from beginning
                if (in_array($job->status, ['phase_1', 'phase_2'])) {
                    $job->update([
                        'started_at'     => now(),
                        'phase_1_lsi'    => null,
                        'phase_1_draft'  => null,
                        'phase_2_critique' => null,
                        'phase_4_answers'  => null,
                        'phase_5_combined' => null,
                        'phase_6_html'     => null,
                    ]);
                }

                // ── Step 2: Run current phase INLINE (not via queue) ──
                $processJob = new \App\Jobs\Ai\ProcessAiGenerationJob(
                    $content->id, $job->id, $targetStatus
                );
                $processJob->handle();

                $job->refresh();
                $logs[] = ['level' => 'success', 'message' => "✅ Fase selesai. Status: {$job->status}"];

                return [
                    'success' => true,
                    'status'  => 'continue',
                    'keyword' => $keyword,
                    'logs'    => $logs,
                ];
            } finally {
                if (isset($lock)) {
                    $lock->release();
                }
            }
        } catch (\Throwable $e) {
            if (isset($lock)) {
                $lock->release();
            }
            $errorMsg = 'FATAL: ' . $e->getMessage();
            if (isset($job) && $job) {
                $job->update(['status' => 'failed', 'error_log' => ['reason' => $e->getMessage()]]);
            }
            if (isset($content) && $content) {
                $content->update(['status' => 'failed_cqi']);
            }
            return ['success' => false, 'error' => $errorMsg, 'logs' => []];
        }
    }




